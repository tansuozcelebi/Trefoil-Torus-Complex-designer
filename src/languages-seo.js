/**
 * Generate HTML for SEO content container
 * @returns {string} HTML fragment with all language content
 */
export function generateSeoHtml() {
  let html = '<div style="display:none;">\n';
  
  seoLanguages.forEach(lang => {
    const langAttr = lang.lang ? ` lang="${lang.lang}"` : '';
    html += `  <h2${langAttr}>${lang.title}</h2>\n`;
    lang.paragraphs.forEach(para => {
      html += `  <p${langAttr}>${para}</p>\n`;
    });
    html += '\n';
  });
  
  html += '</div>';
  return html;
}

/**
 * Inject SEO content into page
 * Call this after DOM is ready
 */
export function injectSeoContent() {
  const seoContainer = document.createElement('div');
  seoContainer.innerHTML = generateSeoHtml();
  document.body.appendChild(seoContainer);
}
